# DXF Export
# ---------------------------------------------------------------------------

def generate_dxf(
    segments: list[dict],
    ppi: float,
    title: str = "WeldScan 3D",
) -> str:
    """
    Generate a CNC-ready DXF file from contour segments.

    Each segment becomes a LINE entity in model space.
    Coordinates are converted from pixels to inches (the DXF drawing unit).

    Args:
        segments: list of {startPoint:{x,y}, endPoint:{x,y}} from edge_detection
        ppi:      pixels-per-inch calibration constant
        title:    document title (stored in DXF header)

    Returns:
        DXF file content as a string (UTF-8)
    """
    doc = ezdxf.new(dxfversion="R2010")
    doc.header["$INSUNITS"] = 1  # 1 = inches

    msp = doc.modelspace()

    for seg in segments:
        x1 = seg["startPoint"]["x"] / ppi
        y1 = -seg["startPoint"]["y"] / ppi   # invert Y (screen → CAD coords)
        x2 = seg["endPoint"]["x"] / ppi
        y2 = -seg["endPoint"]["y"] / ppi
        msp.add_line((x1, y1), (x2, y2))

    buf = io.StringIO()
    doc.write(buf)
    return buf.getvalue()
